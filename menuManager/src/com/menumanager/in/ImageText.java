package com.menumanager.in;

public class ImageText {
	    private String imageUrl;
	    private String text;
	 
	    public ImageText(String imageUrl, String text) {
	        this.imageUrl = imageUrl;
	        this.text = text;
	    }
	    public String getImageUrl() {
	        return imageUrl;
	    }
	    public String getText() {
	        return text;
	    }
	}

